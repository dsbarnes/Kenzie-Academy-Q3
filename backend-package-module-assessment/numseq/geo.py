def square(n):
    """ Returns the square (N ** 2) of a number """
    return n ** 2


def triangle(n):
    """ Sum of all numbers <= N """
    return int((n / 2) * (n + 1))


def cube(n):
    """ Returns the cube (N ** 3) of a number """
    return n ** 3
